.MODEL SMALL
.STACK 100h
.DATA
  question DB 'ENTER ALPHABET:$'
  upperCase DB 'UPPER CASE$'
  lowerCase DB 'LOWER CASE$'
.CODE
MAIN PROC
  MOV AX, @DATA
  MOV DS,AX
  LEA DX,question
  MOV AH, 09H
  INT 21H
  MOV AH,01H
  INT 21H
  MOV BH,AL
  MOV AH,02H
  MOV DL,0AH
  INT 21H
  MOV DL,0DH
  INT 21H
  CMP BH,5AH
  JL UPPER
  JG LOWER
UPPER:
LEA DX,upperCase
  MOV AH,09H
  INT 21H
  JMP EXIT
LOWER:
  LEA DX,lowerCase
  MOV AH,09H
  INT 21H
EXIT:
MAIN ENDP
END MAIN